#include "mathematics.h"
#include <math.h>

double Sum(double& a, double& b)
{
	return a+b;
}

double Sub(double& a, double& b)
{
	return a-b;
}

double Div(double& a, double& b)
{
	return a/b;
}

double Mult(double& a, double& b)
{
	return a * b;
}

double Exp(double& a, double& b)
{
	return pow(a, b);
}
